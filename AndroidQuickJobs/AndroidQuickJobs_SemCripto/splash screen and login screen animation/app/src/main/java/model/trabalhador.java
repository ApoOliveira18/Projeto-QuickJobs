package model;

public class trabalhador {

    private int id_trabalhador;
    private int id_tipo_pacote;
    private String nome;
    private String foto;
    private String cpf;
    private String sexo;
    private String tel;
    private String email;
    private String senha;
    private String cep;
    private String num_residencial;
    private String complemento;
    private String data_nasc;
    private String data_registro;

    public int getId_trabalhador() {
        return id_trabalhador;
    }

    public void setId_trabalhador(int id_trabalhador) {
        this.id_trabalhador = id_trabalhador;
    }

    public int getId_tipo_pacote() {
        return id_tipo_pacote;
    }

    public void setId_tipo_pacote(int id_tipo_pacote) {
        this.id_tipo_pacote = id_tipo_pacote;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public String getNum_residencial() {
        return num_residencial;
    }

    public void setNum_residencial(String num_residencial) {
        this.num_residencial = num_residencial;
    }

    public String getComplemento() {
        return complemento;
    }

    public void setComplemento(String complemento) {
        this.complemento = complemento;
    }

    public String getData_nasc() {
        return data_nasc;
    }

    public void setData_nasc(String data_nasc) {
        this.data_nasc = data_nasc;
    }

    public String getData_registro() {
        return data_registro;
    }

    public void setData_registro(String data_registro) {
        this.data_registro = data_registro;
    }
}
